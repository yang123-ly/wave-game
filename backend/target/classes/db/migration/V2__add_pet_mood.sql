-- 创建 pet 表（如不存在）
CREATE TABLE IF NOT EXISTS `pet` (
    `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '宠物ID',
    `user_id` BIGINT NOT NULL COMMENT '所属用户ID',
    `name` VARCHAR(50) NOT NULL COMMENT '宠物名称',
    `species` VARCHAR(50) NOT NULL COMMENT '宠物种类',
    `level` INT NOT NULL DEFAULT 1 COMMENT '等级',
    `exp` INT NOT NULL DEFAULT 0 COMMENT '经验值',
    `avatar` VARCHAR(255) DEFAULT NULL COMMENT '头像',
    `mood` INT NOT NULL DEFAULT 80 COMMENT '心情值 0-100',
    `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`id`),
    KEY `idx_user_id` (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='宠物表';
